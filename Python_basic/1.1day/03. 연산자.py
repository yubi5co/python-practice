# 연산자 
print(1+1)            # 2
print(3*2)            # 6
print(9/3)            # 3.0

print(2**4)           # 2의 4제곱 '16'
print(5%3)            # 나머지 '2'
print(45/8)           # 나누기 '5.625'

print(1!=3)           # !: 부정 --> != 같지않다는 의미 'True'
print(not(1!=3))      # not(True) --> False

print((3>0)and(5<6))  # and = &
print((3>0)&(5<6))

print((3>0)or(3<5))   # or = |
print((3>0)|(3>5))

print(5>4>3)
print(5>4>7)
# 숫자 자료형 
# 개발을 할 때에는 저장을 하면서 실행하는 습관을 가지는게 좋다.
print(3)
print(-20)
print(4000)
print(4+3)
print(4*3)
print(3*(3+2))

# 문자 자료형 
print('apple')
print("apple")         # 파이썬에서는 ''와""는 같다.

# boolean 자료형 (참과 거짓을 나타내는 유형)
print(5>10)                   # False
print(5<10)                   # True
print(not True)               # False
print(not False)              # True
print(not(5>10))              # not(False) --> True

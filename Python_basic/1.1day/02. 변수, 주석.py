# 변수부분
country = '한국'
city = '제주'
popul = 60
bigcity = popul >= 100


print(country+"에는"+city+"라는 지역이 있어요.")     # + : 문장을 연결
print(city+"의 인구는"+str(popul)+"만명 이에요")    # str: 정수형을 문자형으로 변환 
print(city+"는 광역시인가요?" +str(bigcity))


